// Level 13 — `for` loops practice (10 different ranges)
// TODO: Write 10 different for loops that print iterated numbers with console.log.
// 1. Counting 1 to 5
for (let i = 1; i <= 5; i++) {
  console.log(i);
}

// 2. Counting 0 to 9
for (let i = 0; i < 10; i++) {
  console.log(i);
}

// 3. Counting backwards from 5 down to 1
for (let i = 5; i >= 1; i--) {
  console.log(i);
}

// 4. Counting by twos (even numbers)
for (let i = 0; i <= 10; i += 2) {
  console.log(i);
}

// 5. Counting by fives
for (let i = 0; i <= 20; i += 5) {
  console.log(i);
}

// 6. Starting at 10 and ending at 15
for (let i = 10; i <= 15; i++) {
  console.log(i);
}

// 7. Counting from -3 to 0
for (let i = -3; i <= 0; i++) {
  console.log(i);
}

// 8. Counting by tens up to 100
for (let i = 10; i <= 100; i += 10) {
  console.log(i);
}

// 9. Counting backwards from 10 down to 0
for (let i = 10; i >= 0; i--) {
  console.log(i);
}

// 10. A short range of just three numbers
for (let i = 50; i <= 52; i++) {
  console.log(i);
}
